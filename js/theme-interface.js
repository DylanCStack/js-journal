
$(document).ready(function(){
  $("#red").click(function(){
    $("body").css({"background-color":"#992020", "color":"black"});
  });
  $("#blue").click(function(){
    $("body").css({"background-color":"#202099", "color":"black"});
  });
  $("#black").click(function(){
    $("body").css({"background-color":"#000000", "color":"white"});
  });
});
